    private void modulesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modulesActionPerformed
        String selectedModuleName = (String) modules.getSelectedItem();
        if (selectedModuleName == null || selectedModuleName.equals(MODULE_PLACEHOLDER)) {
            selectedModuleId = null;  // Clear the selected module ID
            resetQuizDropdown();
            return;
        }

        selectedModuleId = moduleNameToId.get(selectedModuleName);  // Store the module ID
        if (selectedModuleId != null) {
            loadQuizzesForModule(selectedModuleId);
            modules.setForeground(Color.BLACK);
        } else {
            selectedModuleId = null;  // Clear if not found
            resetQuizDropdown();
        }
    }//GEN-LAST:event_modulesActionPerformed

    private void quizActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_quizActionPerformed
        String selectedQuizName = (String) quiz.getSelectedItem();
        if (selectedQuizName != null && !selectedQuizName.equals(QUIZ_PLACEHOLDER)) {
            quiz.setForeground(Color.BLACK);
            String quizId = quizNameToId.get(selectedQuizName);
            if (quizId != null && selectedModuleId != null) {
                updateReportForQuiz(selectedModuleId, quizId);  // Pass both module ID and quiz ID
            } else {
                resetReportLabels();
            }
        } else {
            resetReportLabels();
        }
    }//GEN-LAST:event_quizActionPerformed

    private void loadModulesFromFile() {
        modules.removeAllItems();
        moduleNameToId.clear();
        modules.addItem(MODULE_PLACEHOLDER);  // Always add placeholder as first item
        try (BufferedReader reader = new BufferedReader(new FileReader("src\\main\\java\\oopwj\\modules.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String trimmed = line.trim();
                if (!trimmed.isEmpty()) {
                    String[] parts = trimmed.split(",");
                    if (parts.length >= 3) {
                        String moduleId = parts[0].trim();
                        String moduleName = parts[1].trim();
                        String moduleACID = parts[2].trim();  // Academic Leader ID
                        
                        // Filter modules: only show modules under this Academic Leader
                        if (this.academicLeaderID != null && !moduleACID.equals(this.academicLeaderID)) {
                            continue;  // Skip modules not belonging to this AC
                        }

                        modules.addItem(moduleName);
                        moduleNameToId.put(moduleName, moduleId);
                    }
                }
            }
        } catch (IOException e) {
            logger.log(java.util.logging.Level.WARNING, "Unable to load modules list", e);
            modules.addItem("Error loading modules");
        }
        modules.setSelectedIndex(0);  // Set placeholder as default selection
    }

    private void loadQuizzesForModule(String moduleId) {
        quiz.removeAllItems();
        quizNameToId.clear();
        quiz.addItem(QUIZ_PLACEHOLDER);  // Always add placeholder as first item
        try (BufferedReader reader = new BufferedReader(new FileReader("src\\main\\java\\oopwj\\Quiz.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String trimmed = line.trim();
                if (!trimmed.isEmpty()) {
                    String[] parts = trimmed.split(",");
                    if (parts.length >= 3) {
                        String quizId = parts[0].trim();
                        String quizModuleId = parts[1].trim();
                        String quizName = parts[2].trim();
                        if (quizModuleId.equals(moduleId)) {
                            quiz.addItem(quizName);
                            quizNameToId.put(quizName, quizId);
                        }
                    }
                }
            }
        } catch (IOException e) {
            logger.log(java.util.logging.Level.WARNING, "Unable to load quiz list", e);
            quiz.addItem("Error loading quiz");
        }
        quiz.setForeground(Color.GRAY);
        quiz.setSelectedIndex(0);  // Set placeholder as default selection
    }

    private void loadStudentNamesFromFile() {
        studentIdToName.clear();
        try (BufferedReader reader = new BufferedReader(new FileReader("src\\main\\java\\oopwj\\student.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String trimmed = line.trim();
                if (!trimmed.isEmpty()) {
                    String[] parts = trimmed.split(",");
                    if (parts.length >= 2) {
                        String studentId = parts[0].trim();
                        String studentName = parts[1].trim();
                        studentIdToName.put(studentId, studentName);
                    }
                }
            }
        } catch (IOException e) {
            logger.log(java.util.logging.Level.WARNING, "Unable to load student list", e);
        }
    }

    private void updateReportForQuiz(String moduleId, String quizId) {
        int totalStudents = 0;
        int passedStudents = 0;
        Integer highest = null;
        Integer lowest = null;
        String highestStudentId = "";
        String lowestStudentId = "";
        int[] gradeCounts = new int[7]; // A+, A, B+, B, C, D, F

        try (BufferedReader reader = new BufferedReader(new FileReader("src\\main\\java\\oopwj\\FinalGrade.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String trimmed = line.trim();
                if (trimmed.isEmpty()) {
                    continue;
                }
                String[] parts = trimmed.split(",");
                if (parts.length >= 5) {
                    String studentId = parts[0].trim();
                    String moduleIdInFile = parts[1].trim();  // Get module ID from file
                    String quizIdInFile = parts[2].trim();
                    String markStr = parts[3].trim();
                    String grade = parts[4].trim();

                    // Check BOTH module ID and quiz ID match
                    if (!moduleIdInFile.equals(moduleId) || !quizIdInFile.equals(quizId)) {
                        continue;
                    }

                    int mark;
                    try {
                        mark = Integer.parseInt(markStr);
                    } catch (NumberFormatException ex) {
                        continue;
                    }

                    totalStudents++;
                    if (!"F".equalsIgnoreCase(grade)) {
                        passedStudents++;
                    }

                    String gradeUpper = grade.trim().toUpperCase();
                    switch (gradeUpper) {
                        case "A+":
                            gradeCounts[0]++;
                            break;
                        case "A":
                            gradeCounts[1]++;
                            break;
                        case "B+":
                            gradeCounts[2]++;
                            break;
                        case "B":
                            gradeCounts[3]++;
                            break;
                        case "C":
                            gradeCounts[4]++;
                            break;
                        case "D":
                            gradeCounts[5]++;
                            break;
                        case "F":
                            gradeCounts[6]++;
                            break;
                        default:
                            break;
                    }

                    if (highest == null || mark > highest) {
                        highest = mark;
                        highestStudentId = studentId;
                    }
                    if (lowest == null || mark < lowest) {
                        lowest = mark;
                        lowestStudentId = studentId;
                    }
                }
            }
        } catch (IOException e) {
            logger.log(java.util.logging.Level.WARNING, "Unable to load final grades", e);
        }

        if (totalStudents == 0) {
            resetReportLabels();
            return;
        }

        double passRateValue = (passedStudents * 100.0) / totalStudents;
        passRate.setText(String.format("%.2f%%", passRateValue));
        
        // Set color based on pass rate
        if (passRateValue >= 70) {
            passRate.setForeground(new Color(0, 128, 0));  // Green
        } else if (passRateValue >= 50) {
            passRate.setForeground(new Color(255, 193, 7));  // Yellow
        } else {
            passRate.setForeground(new Color(255, 0, 0));  // Red
        }
        
        // Set student count label
        numberOfStudents.setText(passedStudents + " out of " + totalStudents + " students");

        updateGradeTable(gradeCounts);

        if (highest != null) {
            highestMark.setText(String.valueOf(highest));
            studentIDHighest.setText(highestStudentId);
            studentNameHighest.setText(studentIdToName.getOrDefault(highestStudentId, "Unknown"));
        }

        if (lowest != null) {
            lowestMark.setText(String.valueOf(lowest));
            studentIDLowest.setText(lowestStudentId);
            studentNameLowest.setText(studentIdToName.getOrDefault(lowestStudentId, "Unknown"));
        }
    }

