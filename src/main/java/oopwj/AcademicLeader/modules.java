    private void addModuleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addModuleActionPerformed
        // TODO add your handling code here:
        String nextId = getNextModuleId();

        JTextField moduleNameField = new JTextField();
        JComboBox<String> lecturerCombo;

        if (lecturers.isEmpty()) {
            lecturerCombo = new JComboBox<>(new String[]{"NONE"});
        } else {
            java.util.List<String> options = new ArrayList<>(lecturers);
            options.add(0, "NONE");
            lecturerCombo = new JComboBox<>(options.toArray(new String[0]));
        }

        JPanel panel = new JPanel(new GridLayout(0, 1));
        panel.add(new JLabel("Module name:"));
        panel.add(moduleNameField);
        panel.add(new JLabel("Lecturer:"));
        panel.add(lecturerCombo);

        int result = JOptionPane.showConfirmDialog(this, panel, "Add Module", JOptionPane.OK_CANCEL_OPTION, JOptionPane.PLAIN_MESSAGE);
        if (result != JOptionPane.OK_OPTION) {
            return;
        }

        String newModuleName = moduleNameField.getText().trim();
        String selectedLecturerName = (String) lecturerCombo.getSelectedItem();
        if (selectedLecturerName == null) {
            selectedLecturerName = "NONE";
        }

        if (newModuleName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Module name cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        model.addRow(new Object[]{nextId, newModuleName, selectedLecturerName});

        saveData();
        JOptionPane.showMessageDialog(this, "New module added successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_addModuleActionPerformed

