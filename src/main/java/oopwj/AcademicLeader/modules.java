    private void saveData () {
        try {
            List<String> allLines = new ArrayList<>();
            Map<String, int[]> currentModuleMap = new HashMap<>();
            
            try (BufferedReader reader = new BufferedReader(new FileReader("src\\main\\java\\oopwj\\Data\\modules.txt"))) {
                String line;
                while ((line = reader.readLine()) != null) {
                    String trimmed = line.trim();
                    if (!trimmed.isEmpty()) {
                        allLines.add(trimmed);
                    }
                }
            } catch (IOException e) {
            }
            
            for (int i = 0; i < model.getRowCount(); i++) {
                String moduleId = model.getValueAt(i, 0).toString();
                currentModuleMap.put(moduleId, new int[]{i, 1});
            }
            
            try (BufferedWriter writer = new BufferedWriter(new FileWriter("src\\main\\java\\oopwj\\Data\\modules.txt"))) {
                for (String line : allLines) {
                    String[] parts = line.split(",");
                    if (parts.length >= 3) {
                        String moduleId = parts[0].trim();
                        String academicLeaderId = parts[2].trim();
                        
                        if (academicLeaderId.equals(this.academicLeaderID)) {
                            int[] modelInfo = currentModuleMap.get(moduleId);
                            if (modelInfo != null && modelInfo[1] == 1) {
                                int modelRow = modelInfo[0];
                                String moduleName = model.getValueAt(modelRow, 1).toString();
                                String lecturerName = model.getValueAt(modelRow, 2).toString();
                                String lecturerId = lecturerNameToId.getOrDefault(lecturerName, lecturerName);
                                writer.write(moduleId + "," + moduleName + "," + this.academicLeaderID + "," + lecturerId);
                                writer.newLine();
                                modelInfo[1] = 0;
                            }
                        } else {
                            writer.write(line);
                            writer.newLine();
                        }
                    }
                }
                
                for (int i = 0; i < model.getRowCount(); i++) {
                    String moduleId = model.getValueAt(i, 0).toString();
                    int[] modelInfo = currentModuleMap.get(moduleId);
                    if (modelInfo != null && modelInfo[1] == 1) {
                        String moduleName = model.getValueAt(i, 1).toString();
                        String lecturerName = model.getValueAt(i, 2).toString();
                        String lecturerId = lecturerNameToId.getOrDefault(lecturerName, lecturerName);
                        writer.write(moduleId + "," + moduleName + "," + this.academicLeaderID + "," + lecturerId);
                        writer.newLine();
                    }
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "An error occurred while saving the data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void loadLecturersFromFile() {
        lecturers.clear();
        lecturerIdToName.clear();
        lecturerNameToId.clear();
        try (BufferedReader reader = new BufferedReader(new FileReader("src\\main\\java\\oopwj\\Data\\lecturer.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String trimmed = line.trim();
                if (!trimmed.isEmpty()) {
                    String[] parts = trimmed.split(",");
                    if (parts.length >= 5) {
                        String id = parts[0].trim();
                        String name = parts[1].trim();
                        String lecturerACID = parts[4].trim();
                        
                        if (this.academicLeaderID != null && !lecturerACID.equals(this.academicLeaderID)) {
                            continue;
                        }
                        
                        lecturers.add(name);
                        lecturerIdToName.put(id, name);
                        lecturerNameToId.put(name, id);
                    } else if (parts.length >= 2) {
                        String id = parts[0].trim();
                        String name = parts[1].trim();
                        if (this.academicLeaderID == null) {
                            lecturers.add(name);
                            lecturerIdToName.put(id, name);
                            lecturerNameToId.put(name, id);
                        }
                    }
                }
            }
        } catch (IOException e) {
            logger.log(java.util.logging.Level.WARNING, "Unable to load lecturers list", e);
        }
    }

    private String getNextModuleId() {
        int maxId = 0;
        
        try (BufferedReader reader = new BufferedReader(new FileReader("src\\main\\java\\oopwj\\Data\\modules.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String trimmed = line.trim();
                if (!trimmed.isEmpty()) {
                    String[] parts = trimmed.split(",");
                    if (parts.length > 0) {
                        String id = parts[0].trim();
                        if (id.matches("M\\d+")) {
                            try {
                                int numeric = Integer.parseInt(id.substring(1));
                                if (numeric > maxId) {
                                    maxId = numeric;
                                }
                            } catch (NumberFormatException ignored) {
                            }
                        }
                    }
                }
            }
        } catch (IOException e) {
            logger.log(java.util.logging.Level.WARNING, "Unable to read modules file for ID generation", e);
        }
        
        for (int i = 0; i < model.getRowCount(); i++) {
            Object value = model.getValueAt(i, 0);
            if (value != null) {
                String id = value.toString().trim();
                if (id.matches("M\\d+")) {
                    try {
                        int numeric = Integer.parseInt(id.substring(1));
                        if (numeric > maxId) {
                            maxId = numeric;
                        }
                    } catch (NumberFormatException ignored) {
                    }
                }
            }
        }
        
        return String.format("M%03d", maxId + 1);
    }
    
    private void enableSearch() {
        TableRowSorter<DefaultTableModel> moduleSorter =
            new TableRowSorter<>((DefaultTableModel) modulesList.getModel());
        modulesList.setRowSorter(moduleSorter);

        searchBar.getDocument().addDocumentListener(new javax.swing.event.DocumentListener() {
            @Override
            public void insertUpdate(javax.swing.event.DocumentEvent e) { filter(); }
            @Override
            public void removeUpdate(javax.swing.event.DocumentEvent e) { filter(); }
            @Override
            public void changedUpdate(javax.swing.event.DocumentEvent e) { filter(); }

            private void filter() {
                String text = searchBar.getText();
                moduleSorter.setRowFilter(text.isEmpty() ? null :
                RowFilter.regexFilter("(?i)" + text));
            }
        });
    }
    

