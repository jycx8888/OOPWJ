    public modules(String academicLeaderID, academicLeader parentWindow) {
        this.academicLeaderID = academicLeaderID;
        this.parentWindow = parentWindow;
        try{
        model.setColumnIdentifiers(columnNames);
        loadLecturersFromFile();
        FileReader fr = new FileReader("src\\main\\java\\oopwj\\Data\\modules.txt");
        BufferedReader br = new BufferedReader(fr);
        
        String line = null;
        
        while ((line = br.readLine()) != null){
            String values[] = line.split(",");
            for (int i = 0; i < values.length; i++) {
                values[i] = values[i].trim();
            }
            
            if (values.length >= 3) {
                String moduleACID = values[2];
                
                if (this.academicLeaderID != null && !moduleACID.equals(this.academicLeaderID)) {
                    continue;
                }
                
                String lecturerName = lecturerIdToName.getOrDefault(values[3], values[3]);
                model.addRow(new Object[]{values[0], values[1], lecturerName});
            } else if (values.length == 2) {
                if (this.academicLeaderID == null) {
                    model.addRow(new Object[]{values[0], values[1], "NONE"});
                }
            }
        }
        br.close();
        fr.close();
        
        } catch(Exception e){
            
        }
       
        
        initComponents();
        setLocationRelativeTo(null);
        enableSearch();
        addHoverEffect(addModule, new Color(70,130,180), new Color(100,149,237));
        addHoverEffect(editModule, new Color(70,130,180), new Color(100,149,237));
        addHoverEffect(deleteModule, new Color(70,130,180), new Color(100,149,237));
        addHoverEffect(exit, new Color(220,80,80), new Color(255,120,120));
    }
    
