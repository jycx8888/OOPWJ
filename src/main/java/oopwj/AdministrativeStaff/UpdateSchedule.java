    private void addHoverEffect(JButton btn, Color normal, Color hover) {
        btn.setOpaque(true);
        btn.setContentAreaFilled(true);
        btn.setBorderPainted(false);
        btn.setFocusPainted(false);

        btn.setBackground(normal);
        btn.setForeground(Color.WHITE);

        btn.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseEntered(java.awt.event.MouseEvent e) {
                btn.setBackground(hover);
            }

            @Override
            public void mouseExited(java.awt.event.MouseEvent e) {
                btn.setBackground(normal);
            }
        });
    }
    
    private void setTime(JSpinner spinner, String timeStr) {
    try {
        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm");
        spinner.setValue(sdf.parse(timeStr));
    } catch (ParseException e) {
        spinner.setValue(new Date());
    }
}
    
    private void loadClassrooms(String currentClassId) {
    cboClassroom.removeAllItems();

    File classFile = new File("src\\main\\java\\oopwj\\Data\\class.txt");

    try (BufferedReader br = new BufferedReader(new FileReader(classFile))) {
        String line;
        while ((line = br.readLine()) != null) {
            String[] data = line.split(",");
            String classId = data[0];
            String className = data[1];

            cboClassroom.addItem(classId + " - " + className);

            if (classId.equals(currentClassId)) {
                cboClassroom.setSelectedItem(classId + " - " + className);
            }
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Failed to load classrooms.");
    }
}
    
