    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        String selected = cboClassroom.getSelectedItem().toString();
        String newClassId = selected.split(" - ")[0];

        Date newDateObj = dateChoose.getDate();
        if (newDateObj == null) {
            JOptionPane.showMessageDialog(this, "Please select a date.");
            return;
        }

        SimpleDateFormat dateFmt = new SimpleDateFormat("dd/MM/yyyy");
        String newDate = dateFmt.format(newDateObj);

        Date newStart = (Date) spinMin.getValue();
        Date newEnd = (Date) spinMax.getValue();

        if (!newStart.before(newEnd)) {
            JOptionPane.showMessageDialog(this,
                    "Start time must be before end time.");
            return;
        }

        SimpleDateFormat timeFmt = new SimpleDateFormat("HH:mm");
        String newStartStr = timeFmt.format(newStart);
        String newEndStr = timeFmt.format(newEnd);

        File inputFile = new File("src\\main\\java\\oopwj\\Data\\class_schedule.txt");
        File tempFile = new File("src\\main\\java\\oopwj\\Data\\class_schedule_temp.txt");

        boolean conflictFound = false;

        try (BufferedReader br = new BufferedReader(new FileReader(inputFile))) {
            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(",");

                String classId = data[0];
                String date = data[2];

                if (!classId.equals(newClassId) || !date.equals(newDate))
                    continue;

                boolean isOriginal =
                        classId.equals(originalClassId) &&
                        date.equals(originalDate) &&
                        data[4].equals(originalStart) &&
                        data[5].equals(originalEnd);

                if (isOriginal) continue;

                Date existingStart = timeFmt.parse(data[4]);
                Date existingEnd = timeFmt.parse(data[5]);

                if (!newEnd.before(existingStart) &&
                    !newStart.after(existingEnd)) {
                    conflictFound = true;
                    break;
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Failed to validate schedule.");
            return;
        }

        if (conflictFound) {
            JOptionPane.showMessageDialog(this,
                    "Schedule conflict detected.\n" +
                    "The selected classroom is not available at this time.");
            return;
        }

        try (BufferedReader br = new BufferedReader(new FileReader(inputFile));
             BufferedWriter bw = new BufferedWriter(new FileWriter(tempFile))) {

            String line;
            while ((line = br.readLine()) != null) {
                String[] data = line.split(",");

                boolean isTarget =
                        data[0].equals(originalClassId) &&
                        data[2].equals(originalDate) &&
                        data[4].equals(originalStart) &&
                        data[5].equals(originalEnd);

                if (isTarget) {
                    bw.write(
                            newClassId + "," +
                            data[1] + "," +
                            newDate + "," +
                            txtDay.getText() + "," +
                            newStartStr + "," +
                            newEndStr
                    );
                } else {
                    bw.write(line);
                }
                bw.newLine();
            }

        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Update failed.");
            return;
        }

        inputFile.delete();
        tempFile.renameTo(inputFile);

        JOptionPane.showMessageDialog(this, "Schedule updated successfully.");
        dispose();
    }//GEN-LAST:event_btnUpdateActionPerformed

